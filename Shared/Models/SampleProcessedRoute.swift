    /// A Route built from `sampleCoordinates`.
    static let sampleRoute: Route = {
        let points = sampleCoordinates.map {
            TrackPoint(lat: $0.lat, lon: $0.lon, elevation: nil)
        }
        return Route(
            id: UUID(),
            name: "Chicago Parks Tour (Sample)",
            points: points)
    }()

    /// A ProcessedRoute version of `sampleRoute`.
    static let processed: ProcessedRoute = RouteProcessor.process(sampleRoute)

    /// Returns a (location, segmentIndex, distanceAlongRoute, heading) for a
    /// rider positioned at the given fraction (0...1) along the sample route.
    static func riderState(at fraction: Double = 0.42) -> (location: CLLocation, segmentIndex: Int, distanceAlongRoute: Double, heading: Double) {
        let route = processed
        let points = route.points
        guard points.count >= 2 else {
            return (
                CLLocation(latitude: 41.94, longitude: -87.66),
                0, 0, 0)
        }
        let clamped = max(0, min(1, fraction))
        let targetDist = route.totalDistance * clamped
        var idx = 0
        for i in 0..<points.count where points[i].distanceFromStart <= targetDist {
            idx = i
        }
        idx = min(idx, points.count - 1)
        let pt = points[idx]
        let bearing = pt.bearingToNext
        let location = CLLocation(
            coordinate: pt.coordinate,
            altitude: pt.elevation ?? 0,
            horizontalAccuracy: 5,
            verticalAccuracy: 5,
            course: bearing,
            speed: 5, // m/s
            timestamp: Date())
        return (location, idx, pt.distanceFromStart, bearing)
    }
